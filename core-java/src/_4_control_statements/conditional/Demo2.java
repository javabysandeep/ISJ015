package _4_control_statements.conditional;

public class Demo2 {
    public static void main(String[] args) {
        int number = 11;

        if (number % 2 == 0) {
            System.out.println("its an even number");
        }

        if (number % 2 != 0) {
            System.out.println("its an odd number");
        }

    }
}
