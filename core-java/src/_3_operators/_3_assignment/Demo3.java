package _3_operators._3_assignment;

public class Demo3 {
    public static void main(String[] args) {
        int sum = 1;
        sum = sum + 1;
        System.out.println("sum is " + sum);//2

        int b = 100;
        b = b - 20;
        System.out.println("b is " + b);//80

        int c = 10;
        c = c * 20;
        System.out.println("c is " + c);//200

        int d = 20;
        d = d / 2;
        System.out.println("d is " + d);//10

    }
}
